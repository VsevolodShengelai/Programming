#Питон относится к языкам с неявной сильной динамической типизацией.
#Неявная типизация означает, что при объявлении переменной не нужно указывать её тип
#Динамическая типизация означает, что тип переменной определяется непосредственно при выполнении программы
#Сильная типизация не позволяет производить операции в выражениях с данными различных типов 
a = 3     # int – целое число
b = 3.14  # float – число с плавающей точкой
print(a)
print(b)
